#define GRAPH_WIDTH 480
#define GRAPH_HEIGHT 300
#define GRAPH_OFFSET_Y 20
#define GRID_COLOR YELLOW

uint32_t ID;
int x_pos = 0;
int lastY = 0;

int main(void)
{
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_CRC_Init();
  MX_TIM1_Init();
  MX_USART1_UART_Init();
  MX_ADC1_Init();
  HAL_InitTick(0);

  HAL_TIM_Base_Start(&htim1);
  ID = readID();

  HAL_Delay(100);
  tft_init (ID);
  setRotation(1);
  fillScreen(BLACK);
  fillRect(0, 0, 480, 28, WHITE);
  printnewtstr(10, 20, BLUE, &mono12x7bold, 1, ">> GRAPH MONITOR");

  while (1)
  {
	  HAL_ADC_Start(&hadc1);
	  HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
	  uint16_t analogValue = HAL_ADC_GetValue(&hadc1);
	  HAL_ADC_Stop(&hadc1);

	  graph(analogValue);
//	  drawGrid();
	  HAL_Delay(20);
  }
}

void drawGrid() {
  int x, y;

  // Gambar garis vertikal
  for (x = 10; x < GRAPH_WIDTH; x += 20) {
    drawLine(x, 32, x, GRAPH_HEIGHT, GRID_COLOR);
  }

  // Gambar garis horizontal
  for (y = 32; y <= GRAPH_HEIGHT; y += 20) {
    drawLine(10, y, GRAPH_WIDTH, y, GRID_COLOR);
  }
}

